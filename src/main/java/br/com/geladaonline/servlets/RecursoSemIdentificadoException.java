package br.com.geladaonline.servlets;

public class RecursoSemIdentificadoException extends Exception {

	public RecursoSemIdentificadoException() {
		super();
	}

	public RecursoSemIdentificadoException(String message) {
		super(message);
	}

	public RecursoSemIdentificadoException(String message, Throwable cause) {
		super(message, cause);
	}

	public RecursoSemIdentificadoException(Throwable cause) {
		super(cause);
	}

}
